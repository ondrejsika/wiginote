<?php

function url_key($nazev)
{
	$a = array("ě","š","č","ř","ž","ý","á","í","ů","ú","ó","Ě","Š","Č","Ř","Ž","Ý","Á","Í","Ů","Ú","Ó","Ň","Ď","Ť","ň","ď","ť"," ");
	$b = array("e","s","c","r","z","y","a","i","u","u","o","e","s","c","r","z","y","a","i","u","u","o","n","d","t","n","d","t","-");
	return str_replace($a, $b, strtolower($nazev));
}

